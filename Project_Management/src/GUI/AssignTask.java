    public static String toText(ArrayList<Integer> IDs){
        String s="";
        for (Integer i:IDs){
            if (i==IDs.get(IDs.size()-1)){
                s+=i.toString();
            }
            else {
                s+=i.toString();
            }
        }
        return s;
    }

